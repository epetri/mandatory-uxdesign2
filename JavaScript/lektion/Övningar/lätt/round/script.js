/* Write a function round that takes a number as argument. Return the number rounded to nearest whole number.
Tip: use the Math object */

function round(x){
  return Math.round(x);
}

console.log("7,7     :" + round(7.7));
console.log("10,4    :" + round(10.4));
console.log("1243.4  :" + round(1243.4));
console.log("444.2   :" + round(444.2));
